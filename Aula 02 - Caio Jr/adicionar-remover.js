//arrays

const array = [5, 2, 3, 4]

console.log("Item original:", array)

// push: Adiciona um intem no final do array
array.push("último item")

console.log("adicionou último item")

// pop: Remover o último ítem do array
array.pop();

console.log("Removeu último item", array)

// unshift: Adiciona um ítem no começo do array
array. unshift("Primeiro ítem")

console.log("Adicionou primeiro item", array)

// shift: Remover o primeiro item do array
array.shift();

console.log("Removeu primeiro item", array)